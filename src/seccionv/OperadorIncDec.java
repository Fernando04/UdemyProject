package seccionv;

public class OperadorIncDec {
    public static void main(String[] args) {

        int predecremento=0;
        int posdecremento=0;

        System.out.println("Este es un pre decremento " + --predecremento);
        System.out.println("Este es un post decremento" + posdecremento--);


        int c;
        c= 5;
        System.out.println("El valor de c es: " + c);
        System.out.println("El valor de c es: " + c++);
        System.out.println("El valor de c es: " + c);

        System.out.println();
        c = 5;
        System.out.println("El valor de 5 es:" + c);
        System.out.println("El valor de 5 es:" + ++c);
        System.out.println("El valor de 5 es:" + c);







    }
}
